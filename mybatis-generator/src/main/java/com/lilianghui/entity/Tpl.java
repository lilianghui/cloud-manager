package com.lilianghui.entity;

public class Tpl {
	private String serviceFtl;
	private String serviceImplFtl;
	private String controllerFtl;
	private String entityFtl;
	private String daoFtl;
	private String mapperFtl;
	public String getServiceFtl() {
		return serviceFtl;
	}
	public void setServiceFtl(String serviceFtl) {
		this.serviceFtl = serviceFtl;
	}
	public String getServiceImplFtl() {
		return serviceImplFtl;
	}
	public void setServiceImplFtl(String serviceImplFtl) {
		this.serviceImplFtl = serviceImplFtl;
	}
	public String getControllerFtl() {
		return controllerFtl;
	}
	public void setControllerFtl(String controllerFtl) {
		this.controllerFtl = controllerFtl;
	}
	public String getEntityFtl() {
		return entityFtl;
	}
	public void setEntityFtl(String entityFtl) {
		this.entityFtl = entityFtl;
	}
	public String getDaoFtl() {
		return daoFtl;
	}
	public void setDaoFtl(String daoFtl) {
		this.daoFtl = daoFtl;
	}
	public String getMapperFtl() {
		return mapperFtl;
	}
	public void setMapperFtl(String mapperFtl) {
		this.mapperFtl = mapperFtl;
	}

}
